package com.lpg.findRoad.mapTest;

public class ViewDistance {

	/**
	 * 视野
	 */
	private int x = 3;
	
	/**
	 * 视野
	 */
	private int y = 3;

	/**
	 * 获取X方向上的视野距离
	 * 
	 * @return
	 */
	public int getX() {
		return x;
	}

	/**
	 * 获取Y方向上的视野距离
	 * 
	 * @return
	 */
	public int getY() {
		return y;
	}

	/**
	 * 设置Y方向上的视野距离
	 * 
	 * @param y
	 */
	public void setY(int y) {
		this.y = y;
	}
}
